use itertools::Itertools;
use proconio::{input, marker::*};
use std::cmp::*;

fn main() {
    input! {
        deg: usize,
        dis: usize,
    };

    let dir = match deg * 10 {
        1125..=3374 => "NNE",
        3375..=5624 => "NE",
        5625..=7874 => "ENE",
        7875..=10124 => "E",
        10125..=12374 => "ESE",
        12375..=14624 => "SE",
        14625..=16874 => "SSE",
        16875..=19124 => "S",
        19125..=21374 => "SSW",
        21375..=23624 => "SW",
        23625..=25874 => "WSW",
        25875..=28124 => "W",
        28125..=30374 => "WNW",
        30375..=32624 => "NW",
        32625..=34874 => "NNW",
        _ => "N",
    };

    let w = match (dis + 3) / 6 {
        0..=2 => 0,
        3..=15 => 1,
        16..=33 => 2,
        34..=54 => 3,
        55..=79 => 4,
        80..=107 => 5,
        108..=138 => 6,
        139..=171 => 7,
        172..=207 => 8,
        208..=244 => 9,
        245..=284 => 10,
        285..=326 => 11,
        327.. => 12,
        _ => unreachable!(),
    };

    let dir = if w == 0 { "C" } else { dir };
    println!("{dir} {w}");
}
